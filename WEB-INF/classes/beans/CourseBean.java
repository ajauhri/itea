package beans;

public class CourseBean {
	int id;
	String name;
	int duration;
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public int getDuration() {
		return duration;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setDuration(int duration) {
		this.duration = duration;
	}
}