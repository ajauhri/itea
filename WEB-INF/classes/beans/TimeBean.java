package beans;

public class TimeBean {
	int hh;
	int mm;
	int ss;
	public int getHh() {
		return hh;
	}
	public int getMm() {
		return mm;
	}
	public int getSs() {
		return ss;
	}
	public void setHh(int hh) {
		this.hh = hh;
	}
	public void setMm(int mm) {
		this.mm = mm;
	}
	public void setSs(int ss) {
		this.ss = ss;
	}
}
